// 002. Write a program to accept values of two numbers and print their addition

function add(a, b) {
  return a + b;
}

function sub(a, b) {
  return a - b;
}

function mul(a, b) {
  return a * b;
}

function div(a, b) {
  return a / b;
}

console.log("Addition :", add(4, 5));
console.log("Subtraction :", sub(4, 5));
console.log("Multiplication  :", mul(4, 5));
console.log("Division  :", div(4, 5));
